ll a,b,c,d;
map<ll,ll> dp;
ll fun(ll n)
{
	if(n==0) return dp[n] = 0;
	if(n==1) return dp[n] = d;
	if(dp.find(n)!=dp.end()) return dp[n];

	ll ans = inf;
	ans=min(ans,fun(n/5) + c*((n/5)!=0) + d*(n%5));
	ans=min(ans,fun(n/5+1) + c + d*(5-(n%5)));

	ans=min(ans,fun(n/3) + b*((n/3)!=0) + d*(n%3));
	ans=min(ans,fun(n/3+1) + b + d*(3-(n%3)));

	ans=min(ans,fun(n/2) + a*((n/2)!=0) + d*(n%2));
	if(n/2+1<n) ans=min(ans,fun(n/2+1) + a + d*(2-(n%2)));

	if((double)n<=(double)ans/(double)d) ans = min(ans,n*d);

	return dp[n]=ans;

}
int solve()
{
	ll n;cin>>n>>a>>b>>c>>d;
	dp.clear();

	cout<<fun(n)<<"\n";
	return 0;
}

